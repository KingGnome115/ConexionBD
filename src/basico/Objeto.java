package basico;

/**
 *
 * @author Kevin
 */
public class Objeto
{
    private int item1;
    private String item2;
    private float item3;

    public Objeto()
    {
    }

    public Objeto(int item1, String item2, float item3)
    {
        this.item1 = item1;
        this.item2 = item2;
        this.item3 = item3;
    }

    /**
     * @return the item1
     */
    public int getItem1()
    {
        return item1;
    }

    /**
     * @param item1 the item1 to set
     */
    public void setItem1(int item1)
    {
        this.item1 = item1;
    }

    /**
     * @return the item2
     */
    public String getItem2()
    {
        return item2;
    }

    /**
     * @param item2 the item2 to set
     */
    public void setItem2(String item2)
    {
        this.item2 = item2;
    }

    /**
     * @return the item3
     */
    public float getItem3()
    {
        return item3;
    }

    /**
     * @param item3 the item3 to set
     */
    public void setItem3(float item3)
    {
        this.item3 = item3;
    }
    
    
    
    
}
